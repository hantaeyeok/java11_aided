package structural.adapter;
//POJO(Plain Old Java Object) : 옛날 부터 내려오는 기본 클래스 자바의 원래 기본 클래스
//

public class Volt {
	private int amount;
	public Volt(int amount) {
		this.amount = amount;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	
	
}
